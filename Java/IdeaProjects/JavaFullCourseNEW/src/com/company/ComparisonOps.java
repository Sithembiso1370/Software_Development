package com.company;

public class ComparisonOps {

    public static void main(String[] args) {


        System.out.println(9 == 9);
        System.out.println(9 != 9);
        System.out.println(9 >= 9);
        System.out.println(9 <= 9);
        System.out.println(9 == 9 && 9 <= 9);
        System.out.println(9 == 9 || 9 >= 9);
    }
}
